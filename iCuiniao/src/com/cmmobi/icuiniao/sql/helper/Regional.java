package com.cmmobi.icuiniao.sql.helper;

public class Regional {
	private int id;
	private String regionalnum;
	private String regionalname;
	private int fid;
	private int regionallevel;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRegionalnum() {
		return regionalnum;
	}
	public void setRegionalnum(String regionalnum) {
		this.regionalnum = regionalnum;
	}
	public String getRegionalname() {
		return regionalname;
	}
	public void setRegionalname(String regionalname) {
		this.regionalname = regionalname;
	}
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public int getRegionallevel() {
		return regionallevel;
	}
	public void setRegionallevel(int regionallevel) {
		this.regionallevel = regionallevel;
	}
}
